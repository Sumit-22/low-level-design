/*
 * Key Concepts of Singleton Pattern:

    Single Instance:
        Ensures that only one object of the class exists.

    Global Access:
        Provides a way to access the single instance globally without creating multiple instances.

    Lazy Initialization (Optional):
        The instance is created only when it is required (on-demand).

    Thread-Safety Considerations:
        In a multi-threaded environment, proper synchronization is required to avoid multiple instance creation.
 */